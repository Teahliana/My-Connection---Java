    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String eid = txtEmployeeID.getText();
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String email = txtEmail.getText();
        String hireDate = txtHireDate.getText();
        String department = jcmbDepartment.getSelectedItem().toString();
        String gender = "F";
        if(jcmbGender.getSelectedItem().toString().equals("Male")){
            gender = "M";
        }
        String salary = txtSalary.getText();
        int salaryInt = Integer.parseInt(salary);
        int regid = getRegionID(jcmbRegion.getSelectedItem().toString());
        try {
            String sql = "INSERT INTO employees(employee_id,first_name,last_name,email,hire_date,department,gender,salary,region_id) VALUES('"+eid+"','"+firstName+"','"+lastName+"','"+email+"','"+hireDate+"','"+department+"','"+gender+"','"+salaryInt+"','"+regid+"')";
            if(mycon.ExecuteData(sql)){
                JOptionPane.showMessageDialog(null, "Employee Record Inserted");
            }else{
                JOptionPane.showMessageDialog(null, "Something Wrong");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

