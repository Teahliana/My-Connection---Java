    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(txtEmployeeID.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Inser Employee ID");
        }else{
            String userid = txtEmployeeID.getText();
            String sql = "SELECT email FROM employees WHERE employee_id = '"+userid+"'";
            String email = "";
            boolean emailOK = false;
            try {
                ResultSet rs = mycon.getData(sql);
                while(rs.next()){
                    email = rs.getString("email");
                    if(!email.equals(null)){
                        emailOK = true;
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            txtEmail.setText(email);
            if(!emailOK){
                txtEmail.setText("No Email Found");
            }
            
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void loadDepartments(){
        try {
            String sql = "SELECT department FROM departments";
            ResultSet rs = mycon.getData(sql);
            Vector v = new Vector();
            while(rs.next()){
                v.add(rs.getString("department"));
            }
            jcmbDepartment.setModel(new DefaultComboBoxModel<>(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadRegions(){
        try {
            String sql = "SELECT * FROM regions";
            ResultSet rs = mycon.getData(sql);
            Vector v = new Vector();
            while(rs.next()){
                v.add(rs.getString("region_id")+","+rs.getString("region")+","+rs.getString("country"));
            }
            jcmbRegion.setModel(new DefaultComboBoxModel<>(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private int getRegionID(String wholeRegionDetails){
        int regionID = 0;
        String regionid[] = wholeRegionDetails.split(",");
        String regid = regionid[0];
        regionID = Integer.parseInt(regid);
        return regionID;
    }
